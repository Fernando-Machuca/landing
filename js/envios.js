// Agregar municipios en el departamento de recolecta seleccionado

function agregarMunR() {
    var puntoR;
    puntoR = document.departamento_recolecta.puntoR[document.departamento_recolecta.puntoR.selectedIndex].value;

    if (puntoR != 0) {
        mis_opts = eval("Opt_" + puntoR);
        num_opts = mis_opts.length;
        document.departamento_recolecta.municipio_recolecta.length = num_opts;

        for (i = 0; i < num_opts; i++) {
            document.departamento_recolecta.municipio_recolecta.options[i].value = mis_opts[i];
            document.departamento_recolecta.municipio_recolecta.options[i].text = mis_opts[i];
        }
    } else {
        document.departamento_recolecta.opt.length = 1;
        document.departamento_recolecta.municipio_recolecta.options[0].value = "-";
        document.departamento_recolecta.municipio_recolecta.options[0].text = "-";
    }
    //    document.departamento_recolecta.municipio_recolecta.options[0].selected = true;

    var munR;
    munR = document.departamento_recolecta.municipio_recolecta.selectedIndex;
    return [puntoR, munR];
}


